function podeEntrarNaAuto(idade) {
    if (idade >= 18) {
        console.log("Você é maior de idade e pode entrar na auto escola!");
    } else {
        console.log("Você é menor de idade e não pode entrar na auto escola até completar 18 anos!")
    }
}

podeEntrarNaAuto(12);

podeEntrarNaAuto(28);
